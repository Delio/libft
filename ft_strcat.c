#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int c;
	
	i = 0;
	c = ft_strlen(s1);
	while (s2[i])
	{
		s1[c + i] = s2[i];
	   	i++;
	}
	s1[c + i] = '\0';
	return (s1);
}
